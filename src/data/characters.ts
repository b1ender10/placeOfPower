export type CharacterQuoteTier = 'base' | 'bronze' | 'silver' | 'gold'

export interface GameCharacter {
  id: string
  name: string
  /** Несколько фраз на каждый уровень инвентаря */
  quotesByTier: Record<CharacterQuoteTier, string[]>
  /** Локальные файлы в `public/characters/` (источники — Commons / enwiki; voytenko/smaev подобраны через поиск Commons). */
  imageUrl: string
}

export function pickRandomQuote(character: GameCharacter, tier: CharacterQuoteTier): string {
  const list = character.quotesByTier[tier]
  if (!list?.length) return character.quotesByTier.base[0] ?? ''
  return list[Math.floor(Math.random() * list.length)]
}

/** Короткий набор фраз по уровням (по 3 на тир) */
function q(
  base: [string, string, string],
  bronze: [string, string, string],
  silver: [string, string, string],
  gold: [string, string, string],
): Record<CharacterQuoteTier, string[]> {
  return { base: [...base], bronze: [...bronze], silver: [...silver], gold: [...gold] }
}

